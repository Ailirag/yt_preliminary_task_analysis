TOOLS_BUDGET_NOTE = ("Все инструменты делят общий бюджет {max_steps} обращений. Не делай лишних "
                     "вызовов; когда данных достаточно — верни финальный JSON без вызова инструментов.")

NO_TOOLS_NOTE = ("Инструменты в этом прогоне недоступны — анализируй по предоставленному контексту, "
                 "отражай ограничения в missing_info/notes.")


def _tools_section(parts: list[str], max_steps: int, code_tools: bool, nav_tools: bool,
                   current_queue: str, allowed_queues: list[str] | None) -> None:
    if code_tools:
        parts.append(CODE_TOOLS_GUIDANCE)
    if nav_tools:
        cq = current_queue or "текущей очереди"
        allowed = ", ".join(allowed_queues) if allowed_queues else cq
        parts.append(NAV_TOOLS_GUIDANCE.format(current_queue=cq, allowed=allowed))
    if code_tools or nav_tools:
        parts.append(TOOLS_BUDGET_NOTE.format(max_steps=max_steps))
    else:
        parts.append(NO_TOOLS_NOTE)


def _target_systems_section(target_systems: list[tuple[str, str]] | None) -> str:
    """Секция про целевую систему(ы) для агентного цикла: как адресовать workspace= в onec-вызовах.
    target_systems — список (workspace, human_name). Пусто/None -> секции нет (одно-воркспейсный режим)."""
    if not target_systems:
        return ""
    names = ", ".join(f"`{ws}`" for ws, _ in target_systems)
    if len(target_systems) == 1:
        ws, name = target_systems[0]
        return (f"Целевая система 1С — «{name}»: её код УЖЕ доступен через инструменты кода (рабочая копия "
                f"сконфигурирована и подключена). Вызывай инструменты НАПРЯМУЮ, БЕЗ параметра workspace. "
                f"НЕ утверждай в отчёте, что рабочая копия/исходный код недоступны или «не сконфигурированы» — "
                f"они доступны; если данных не хватает, ищи глубже (get_object/read_module/read_routine/find_callers).")
    listing = "; ".join(f"«{name}» — `{ws}`" for ws, name in target_systems)
    return ("Задача затрагивает НЕСКОЛЬКО систем 1С (интеграция): " + listing + ". Код ВСЕХ этих систем "
            f"доступен через инструменты. В КАЖДОМ вызове инструмента кода указывай workspace=<имя>, используя "
            f"ТОЛЬКО эти имена: {names}. Сопоставляй объекты правильной системе, не смешивай их. Не пиши, "
            "что код недоступен.")


def bug_system_prompt(max_steps: int, code_tools: bool, nav_tools: bool,
                      current_queue: str = "", allowed_queues: list[str] | None = None,
                      target_systems: list[tuple[str, str]] | None = None) -> str:
    parts = [
        "Ты — ведущий аналитик-разработчик 1С:Предприятие. Твоя задача — предварительный анализ "
        "ошибки из трекера: понять проблему, найти вероятную причину в коде конфигурации, оценить "
        "сложность исправления и подготовить черновик решения. Отвечай на русском языке.",
        INJECTION_GUARD,
    ]
    _tools_section(parts, max_steps, code_tools, nav_tools, current_queue, allowed_queues)
    if code_tools:
        section = _target_systems_section(target_systems)
        if section:
            parts.append(section)
    parts.append(COMPLEXITY_CRITERIA)
    parts.append(ANALYSIS_JSON_SCHEMA_TEXT)
    return "\n\n".join(parts)


